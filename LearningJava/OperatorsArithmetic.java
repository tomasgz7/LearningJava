package LearningJava;

public class OperatorsArithmetic {
    public static void main(String[] args) {
        int number = 10;
        number += 5;
         System.out.println("\nResult of += " + number);
        number -= 8;
         System.out.println("Result of -= " + number);
        number *= 10;
        System.out.println("Result of *= " + number);
        number /= 3;
         System.out.println("Result of /= " + number);
        number %= 4;
        System.out.println("Result of %= " + number);
        
       

    }
}
